//! Initialize state from a binary dump file.
//!
//! This command loads TIP20 storage slots from a binary file and applies them
//! to the genesis state. The binary format is produced by `tempo-xtask generate-state-bloat`.
//!
//! Uses direct cursor writes with periodic commits to keep memory bounded
//! regardless of input file size.

use std::{
    fs::File,
    io::{BufReader, Read as _},
    path::PathBuf,
};

use alloy_primitives::{B256, U256, keccak256, map::HashSet};
use clap::Parser;
use eyre::{Context as _, ensure};
use reth_chainspec::EthereumHardforks;
use reth_cli_commands::common::{AccessRights, CliNodeTypes, EnvironmentArgs};
use reth_db_api::{
    cursor::{DbCursorRO, DbCursorRW},
    tables,
    transaction::DbTxMut,
};
use reth_ethereum::{chainspec::EthChainSpec, tasks::Runtime};
use reth_primitives_traits::{Account, StorageEntry};
use reth_provider::{BlockNumReader, DatabaseProviderFactory};
use reth_storage_api::DBProvider;
use tempo_chainspec::spec::TempoChainSpecParser;
use tracing::info;

/// Magic bytes for the state bloat binary format (8 bytes)
const MAGIC: &[u8; 8] = b"TEMPOSB\x00";

/// Expected format version
const VERSION: u16 = 1;

/// Default number of storage entries to process before committing.
const DEFAULT_COMMIT_INTERVAL: u64 = 2_000_000;

/// Number of entries to read per I/O chunk (256k entries × 64 bytes = 16 MiB).
const ENTRY_READ_CHUNK: usize = 256 * 1024;

/// Initialize state from a binary dump file.
#[derive(Debug, Parser)]
pub(crate) struct InitFromBinaryDump<C: reth_cli::chainspec::ChainSpecParser = TempoChainSpecParser>
{
    #[command(flatten)]
    env: EnvironmentArgs<C>,

    /// Path to the binary state dump file.
    ///
    /// The file should be generated by `tempo-xtask generate-state-bloat`.
    #[arg(value_name = "BINARY_DUMP_FILE")]
    state: PathBuf,

    /// Number of storage entries to process before committing the transaction.
    #[arg(long, default_value_t = DEFAULT_COMMIT_INTERVAL)]
    commit_interval: u64,
}

impl<C: reth_cli::chainspec::ChainSpecParser<ChainSpec: EthChainSpec + EthereumHardforks>>
    InitFromBinaryDump<C>
{
    /// Execute the init-from-binary-dump command.
    pub(crate) async fn execute<N>(self, runtime: Runtime) -> eyre::Result<()>
    where
        N: CliNodeTypes<ChainSpec = C::ChainSpec>,
    {
        info!(target: "tempo::cli", "Tempo init-from-binary-dump starting");

        let environment = self.env.init::<N>(AccessRights::RW, runtime)?;
        let provider_factory = environment.provider_factory;

        let mut provider_rw = provider_factory.database_provider_rw()?;

        // Verify we're at genesis (block 0)
        let last_block = provider_rw.last_block_number()?;
        ensure!(
            last_block == 0,
            "init-from-binary-dump must be run on a freshly initialized database at block 0, \
             but found block {last_block}"
        );

        info!(target: "tempo::cli", path = %self.state.display(), "Loading binary state dump");

        let file = File::open(&self.state)
            .wrap_err_with(|| format!("failed to open {}", self.state.display()))?;
        let mut reader = BufReader::with_capacity(64 * 1024 * 1024, file);

        let commit_interval = self.commit_interval;
        ensure!(
            commit_interval > 0,
            "commit_interval must be greater than 0"
        );
        let mut total_entries = 0u64;
        let mut total_tokens = 0u64;
        let mut total_commits = 0u64;
        let mut entries_since_commit = 0u64;

        // Track addresses for account hashing (small — only token addresses)
        let mut addresses_seen: HashSet<alloy_primitives::Address> = HashSet::default();

        // Reusable I/O buffer for chunked reads
        let mut io_buf: Vec<u8> = vec![0u8; ENTRY_READ_CHUNK * 64];

        // Open cursors scoped to current transaction. Helper recreates them
        // after each commit so we never hold cursors across tx boundaries.
        macro_rules! open_cursors {
            ($provider:expr) => {{
                let tx = $provider.tx_ref();
                let ac = tx.cursor_write::<tables::PlainAccountState>()?;
                let sc = tx.cursor_dup_write::<tables::PlainStorageState>()?;
                let hac = tx.cursor_write::<tables::HashedAccounts>()?;
                let hsc = tx.cursor_dup_write::<tables::HashedStorages>()?;
                (ac, sc, hac, hsc)
            }};
        }

        let (
            mut account_cursor,
            mut storage_cursor,
            mut hashed_account_cursor,
            mut hashed_storage_cursor,
        ) = open_cursors!(provider_rw);

        // Process blocks from binary file
        loop {
            // Try to read header — distinguish clean EOF (0 bytes) from truncation
            let mut header_buf = [0u8; 40];
            let mut header_filled = 0usize;
            while header_filled < 40 {
                match reader.read(&mut header_buf[header_filled..]) {
                    Ok(0) => break,
                    Ok(n) => header_filled += n,
                    Err(e) if e.kind() == std::io::ErrorKind::Interrupted => continue,
                    Err(e) => return Err(e).wrap_err("failed to read block header"),
                }
            }
            if header_filled == 0 {
                break; // clean EOF at block boundary
            }
            ensure!(
                header_filled == 40,
                "truncated block header: got {header_filled} bytes, expected 40"
            );

            // Validate magic
            ensure!(
                &header_buf[..8] == MAGIC,
                "invalid magic bytes in block header"
            );

            // Validate version
            let version = u16::from_be_bytes([header_buf[8], header_buf[9]]);
            ensure!(
                version == VERSION,
                "unsupported binary format version {version}, expected {VERSION}"
            );

            // Skip flags (2 bytes at offset 10)

            // Read address (20 bytes at offset 12)
            let mut address_bytes = [0u8; 20];
            address_bytes.copy_from_slice(&header_buf[12..32]);
            let address = alloy_primitives::Address::from(address_bytes);

            // Read pair count (8 bytes at offset 32)
            let pair_count = u64::from_be_bytes(header_buf[32..40].try_into().unwrap());

            info!(
                target: "tempo::cli",
                %address,
                pair_count,
                "Processing token storage block"
            );

            let hashed_address = keccak256(address);

            // Ensure account exists in plain state and write hashed account
            if addresses_seen.insert(address) {
                if account_cursor.seek_exact(address)?.is_none() {
                    account_cursor.upsert(address, &Account::default())?;
                }
                hashed_account_cursor.upsert(hashed_address, &Account::default())?;
            }

            // Read and insert entries in chunks
            let start = std::time::Instant::now();
            let mut last_log = start;
            let mut processed = 0u64;

            while processed < pair_count {
                let remaining = pair_count - processed;
                let chunk_entries = remaining.min(ENTRY_READ_CHUNK as u64) as usize;
                let chunk_bytes = chunk_entries * 64;

                reader
                    .read_exact(&mut io_buf[..chunk_bytes])
                    .wrap_err("failed to read storage entries")?;

                for chunk in io_buf[..chunk_bytes].chunks_exact(64) {
                    let slot = B256::from_slice(&chunk[..32]);
                    let value = U256::from_be_bytes::<32>(chunk[32..64].try_into().unwrap());

                    // Skip zero values (they represent deletion)
                    if value.is_zero() {
                        processed += 1;
                        continue;
                    }

                    let entry = StorageEntry { key: slot, value };

                    // Write plain storage
                    storage_cursor.upsert(address, &entry)?;

                    // Write hashed storage directly
                    let hashed_entry = StorageEntry {
                        key: keccak256(slot),
                        value,
                    };
                    hashed_storage_cursor.upsert(hashed_address, &hashed_entry)?;

                    total_entries += 1;
                    entries_since_commit += 1;
                    processed += 1;

                    // Commit periodically — drop all cursors so the tx borrow
                    // is released before calling commit().
                    if entries_since_commit >= commit_interval {
                        drop(account_cursor);
                        drop(storage_cursor);
                        drop(hashed_account_cursor);
                        drop(hashed_storage_cursor);

                        provider_rw.commit()?;
                        provider_rw = provider_factory.database_provider_rw()?;
                        total_commits += 1;
                        entries_since_commit = 0;

                        (
                            account_cursor,
                            storage_cursor,
                            hashed_account_cursor,
                            hashed_storage_cursor,
                        ) = open_cursors!(provider_rw);
                    }

                    // Log progress periodically
                    let now = std::time::Instant::now();
                    if now.duration_since(last_log) >= std::time::Duration::from_secs(5) {
                        let pct = (processed as f64 / pair_count as f64) * 100.0;
                        let elapsed = start.elapsed();
                        let pairs_per_sec = processed as f64 / elapsed.as_secs_f64();
                        info!(
                            target: "tempo::cli",
                            %address,
                            progress = format_args!("{processed}/{pair_count} ({pct:.0}%)"),
                            elapsed = ?elapsed,
                            pairs_per_sec = pairs_per_sec as u64,
                            "Inserting storage"
                        );
                        last_log = now;
                    }
                }
            }

            // End-of-block log
            let elapsed = start.elapsed();
            let pairs_per_sec = if elapsed.as_secs_f64() > 0.0 {
                processed as f64 / elapsed.as_secs_f64()
            } else {
                0.0
            };
            let pct = if pair_count == 0 { 0.0 } else { 100.0 };
            info!(
                target: "tempo::cli",
                %address,
                progress = format_args!("{processed}/{pair_count} ({pct:.0}%)"),
                elapsed = ?elapsed,
                pairs_per_sec = pairs_per_sec as u64,
                "Finished token block"
            );

            total_tokens += 1;
        }

        // Drop cursors before final commit
        drop(account_cursor);
        drop(storage_cursor);
        drop(hashed_account_cursor);
        drop(hashed_storage_cursor);

        // Final commit
        provider_rw.commit()?;
        total_commits += 1;

        info!(
            target: "tempo::cli",
            total_tokens,
            total_entries,
            total_commits,
            addresses = addresses_seen.len(),
            "Binary state dump loaded successfully"
        );

        info!(
            target: "tempo::cli",
            "State loaded. The node will compute the state root on startup."
        );

        Ok(())
    }
}
